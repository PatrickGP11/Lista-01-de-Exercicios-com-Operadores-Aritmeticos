//DIVIDE DOIS NUMEROS E EXIBE O RESULTADO
alert("Programa de divisão") //Saída de dados
let numero01 = Number(prompt("Digite um número :")) //Saída e Entrada de dados 
let numero02 = parseInt(prompt("Digite outro número :")) //Saída e Entrada de dados 
divisao = numero01 / numero02 //Processamento
alert("A divisão dos números digitados é : " + divisao.toFixed(2)) //Saída de dados